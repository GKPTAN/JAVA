public class ParametrosGeometria {
    public double altura;
    public double largura;
    public double lado;
    public double raio;

    public ParametrosGeometria(double altura, double largura, double lado, double raio) {
        this.altura = altura;
        this.largura = largura;
        this.lado = lado;
        this.raio = raio;
    }
}
